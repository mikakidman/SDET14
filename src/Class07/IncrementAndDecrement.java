package Class07;

public class IncrementAndDecrement {
    public static void main(String[] args) {
        int num=90;
        num=num+1;
        System.out.println(num);//91

        //another way to increment variable value
        num++;
        System.out.println(num);//92

        int i=10;
        i--;
        i--;
        System.out.println(i);
    }
}
