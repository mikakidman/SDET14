package Class07;

public class ShorthandOperators {
    public static void main(String[] args) {
        int i=10;
        i=i+9;
        System.out.println(i);

        i=i-5;
        System.out.println(i);

        i=i*2;
        System.out.println(i);

        int a=10;
        a+=9;
        System.out.println(a);

    }
}
